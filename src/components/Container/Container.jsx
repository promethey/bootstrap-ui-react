import React from 'react';
import PropTypes from 'prop-types';
import classNames from 'classnames';
import { getSpacingClassNames } from 'utilities/spacing';
import { usePrefix } from 'helpers/prefix';

function Container(props) {
  const {
    style,
    children,
    className,
    isFluid,
    margin,
    marginSm,
    marginMd,
    marginLg,
    marginXl,
    marginXxl,
    marginX,
    marginY,
    mt,
    me,
    mb,
    ms,
    padding,
    paddingSm,
    paddingMd,
    paddingLg,
    paddingXl,
    paddingXxl,
    paddingX,
    paddingY,
    pt,
    pe,
    pb,
    ps,
    bgColor,
    textColor,
    overflow,
    isBorder,
    isVisible,
    isInvisible,
    ...rest
  } = props;

  const BASE_CLASS_NAME = 'container';

  const classes = classNames(
    { [BASE_CLASS_NAME]: !isFluid },
    {
      [usePrefix('bg', bgColor)]: bgColor,
      [usePrefix('text', textColor)]: textColor,
      [usePrefix('overflow', overflow)]: overflow,
      [usePrefix('border', isBorder)]: isBorder && typeof isBorder === 'string',
      border: isBorder,
      [usePrefix(BASE_CLASS_NAME, 'fluid')]: typeof isFluid === 'boolean' && isFluid,
      [usePrefix(BASE_CLASS_NAME, isFluid)]: typeof isFluid === 'string' && isFluid,
      visible: isVisible && !isInvisible,
      invisible: isInvisible && !isVisible,
    },
    getSpacingClassNames(
      'm',
      margin,
      marginSm,
      marginMd,
      marginLg,
      marginXl,
      marginXxl,
      marginX,
      marginY,
      mt,
      me,
      mb,
      ms,
    ),
    getSpacingClassNames(
      'p',
      padding,
      paddingSm,
      paddingMd,
      paddingLg,
      paddingXl,
      paddingXxl,
      paddingX,
      paddingY,
      pt,
      pe,
      pb,
      ps,
    ),
    className,
  );

  return (
    <div style={style} className={classes} {...rest}>
      {children}
    </div>
  );
}

